pub(crate) fn wave_metric_value(metric: WaveMetric, wave: u8, delta_iterations: u16) -> i32 {
    let row = WAVE_METRIC_ROWS
        .iter()
        .find(|row| row.metric == metric)
        .expect("wave metric must be embedded");
    let wave = wave.max(1);
    let wave_index = usize::from(wave.min(4) - 1);
    let mut value = row.waves[wave_index];
    for _ in 0..delta_iterations {
        value = if row.inter_delta > 0 {
            (value + row.inter_delta).min(row.ceiling)
        } else if row.inter_delta < 0 {
            (value + row.inter_delta).max(row.floor)
        } else {
            value
        };
    }
    value
}

